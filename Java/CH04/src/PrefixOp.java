
public class PrefixOp {

	public static void main(String[] args) {
		int num = 7;
		System.out.println(num++);
		System.out.println(num++);
		System.out.println(num);
		
		System.out.println("--------------------");
		
		int num2 = 5;
		System.out.println(++num);
		System.out.println(++num);
		System.out.println(num);
	}

}
